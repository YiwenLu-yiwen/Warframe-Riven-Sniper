import { writeFile } from "node:fs/promises";

const WIKI_RIVEN_DISPOSITIONS =
  "https://warframe.fandom.com/api.php?action=parse&page=Riven_Mods/Weapon_Dispos/All&prop=text&format=json&origin=*";
const WF_STATUS = "https://api.warframestat.us";
const OUTPUT_PATH = new URL("../server/riven-weapons.generated.js", import.meta.url);

const headers = {
  "accept": "application/json",
  "user-agent": "WarframeRivenSniperPrototype/0.1"
};

const variantPrefix = /^(MK1-|Prisma |Mara |Dex |Vaykor |Telos |Synoid |Rakta |Sancti |Secura |Carmine )/i;

function decodeHtml(value) {
  return value
    .replace(/<[^>]+>/g, "")
    .replace(/&amp;/g, "&")
    .replace(/&#039;/g, "'")
    .replace(/&quot;/g, "\"")
    .replace(/&nbsp;/g, " ")
    .trim();
}

function stripDispositionMode(name) {
  return name
    .replace(/\s+\((Primary|Secondary|Atmosphere|Dual Swords|Heavy Blade)\)$/i, "")
    .trim();
}

function marketUrlName(name) {
  return name
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "")
    .replace(/_+/g, "_");
}

function collapseVariantName(name) {
  return name
    .replace(/\b(Prime|Vandal|Wraith)\b/gi, "")
    .replace(variantPrefix, "")
    .replace(/\s+-\s+/g, "-")
    .replace(/\s+/g, " ")
    .trim();
}

function hasVariantName(name) {
  return /\b(Prime|Vandal|Wraith)\b/i.test(name) || variantPrefix.test(name);
}

function inferGroup(sourceName, item) {
  if (/\(Secondary\)/i.test(sourceName)) return "pistol";
  if (/\(Primary\)/i.test(sourceName)) return "rifle";
  if (/\(Atmosphere\)/i.test(sourceName)) return "archgun";

  const searchable = `${item?.category || ""} ${item?.type || ""} ${item?.productCategory || ""}`;
  if (/Arch-Gun|SpaceGuns/i.test(searchable)) return "archgun";
  if (/Robotic|Sentinel/i.test(searchable)) return "robotic";
  if (/Melee|Zaw/i.test(searchable)) return "melee";
  if (/Secondary|Pistol/i.test(searchable)) return "pistol";
  if (/Shotgun/i.test(searchable)) return "shotgun";
  return "rifle";
}

async function getJson(url) {
  const response = await fetch(url, { headers });
  if (!response.ok) throw new Error(`${response.status} ${url}`);
  return response.json();
}

function indexByName(items) {
  const byName = new Map();
  for (const item of items) {
    if (item.name && !byName.has(item.name)) byName.set(item.name, item);
  }
  return byName;
}

function indexByUniqueName(items) {
  const byUniqueName = new Map();
  for (const item of items) {
    if (item.uniqueName && !byUniqueName.has(item.uniqueName)) byUniqueName.set(item.uniqueName, item);
  }
  return byUniqueName;
}

function parseDispositionEntries(html) {
  return [...html.matchAll(/<li>\s*<a[^>]*>([\s\S]*?)<\/a>\s*\((\d+(?:\.\d+)?)\)<\/li>/g)]
    .map(match => ({
      sourceName: decodeHtml(match[1]),
      disposition: Number(match[2])
    }))
    .filter(entry => entry.disposition > 0)
    .map(entry => ({
      ...entry,
      displayName: stripDispositionMode(entry.sourceName)
    }));
}

function serializeCatalog(catalog, sourceCount) {
  const rows = JSON.stringify(catalog, null, 2);
  return `// Generated by scripts/build-riven-weapon-catalog.mjs.
// Sources: Warframe Wiki Riven disposition table + Warframe Status localized item API.
// Source disposition rows: ${sourceCount}

export const generatedWeaponCatalog = ${rows};
`;
}

const [
  wiki,
  englishWeapons,
  chineseWeapons,
  englishItems,
  chineseItems
] = await Promise.all([
  getJson(WIKI_RIVEN_DISPOSITIONS),
  getJson(`${WF_STATUS}/weapons?language=en`),
  getJson(`${WF_STATUS}/weapons?language=zh`),
  getJson(`${WF_STATUS}/items?language=en`),
  getJson(`${WF_STATUS}/items?language=zh`)
]);

const entries = parseDispositionEntries(wiki.parse.text["*"]);
const dispositionNames = new Set(entries.map(entry => entry.displayName));
const englishByName = indexByName([...englishItems, ...englishWeapons]);
const chineseByUniqueName = indexByUniqueName([...chineseItems, ...chineseWeapons]);
const byFamily = new Map();

for (const entry of entries) {
  const baseName = collapseVariantName(entry.displayName);
  const shouldCollapse =
    hasVariantName(entry.displayName) &&
    baseName &&
    dispositionNames.has(baseName);
  const family = shouldCollapse ? baseName : entry.displayName;
  const item =
    englishByName.get(family) ||
    englishByName.get(entry.displayName) ||
    englishByName.get(baseName);
  const zhItem = item?.uniqueName ? chineseByUniqueName.get(item.uniqueName) : null;
  const zh = zhItem?.name || family;
  const group = inferGroup(entry.sourceName, item);
  const existing = byFamily.get(family);

  if (!existing) {
    byFamily.set(family, {
      family,
      zh,
      group,
      marketUrlName: marketUrlName(family),
      disposition: entry.disposition
    });
    continue;
  }

  existing.disposition = Math.max(existing.disposition, entry.disposition);
  if (existing.zh === existing.family && zh !== family) existing.zh = zh;
}

const catalog = [...byFamily.values()].sort((a, b) => a.family.localeCompare(b.family));
await writeFile(OUTPUT_PATH, serializeCatalog(catalog, entries.length), "utf8");

console.log(`Wrote ${catalog.length} Riven weapon families to ${OUTPUT_PATH.pathname}.`);
